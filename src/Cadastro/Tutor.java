package Cadastro;

public class Tutor {
	private String nome;
	private String endereço;
	
	public Tutor(String nome, String endereço) {
		super();
		this.nome = nome;
		this.endereço = endereço;
	}

	public String getNome() {
		return nome;
	}
	
	public String getEndereço() {
		return endereço;
	}

}
